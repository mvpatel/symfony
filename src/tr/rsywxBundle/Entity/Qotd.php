        return $this->id;
    }

    /**
     * Set quote
     *
     * @param string $quote
     * @return Qotd
     */
    public function setQuote($quote)
    {
        $this->quote = $quote;
    
        return $this;
    }

    /**
     * Get quote
     *
     * @return string 
     */
    public function getQuote()
    {
        return $this->quote;
    }

    /**
     * Set source
     *
     * @param string $source
     * @return Qotd
     */
    public function setSource($source)
